                <h2 class="text-color-dark font-weight-normal text-5 mb-2">Project <strong class="font-weight-extra-bold">Description</strong></h2>

                <p>This project is an AI&Art exhibition that took place in Cluj Napoca. It was a colaboration between Tech Wave and White Hog Design</p>

                <h1>Transparency<br><span style="font-size: 18px">by Ioana Cheres from Tech Wave</span></h1>
                <p>
                    The hologram that the viewer sees floating in the middle of the room is a deepfake of itself. A picture of the viewer is taken before entering the installation, and using deep learning the gender, ethnicity and age of the viewer are computed. Using this data the algorithm tries to determine the piece of information that the viewer is most probably susceptible to. Using the data a deepfake of the viewer is rendered in the hologram, depicting him/her/them saying the words that spread the misinformation. The face of the viewer animated and projected on the hologram is used to create a strong and shocking emotional bond with the information presented, challenging the user to dismiss information that comes from it’s own persona.”
                </p>

                <h1>Virtual Reality<br><span style="font-size: 18px">by White Hog Design</span></h1>
                <p>
                    Virtual Reality (VR) is a computer-generated environment with scenes and objects that appear to be real, making the user feel they are immersed in their surroundings. This environment is perceived through a device known as a Virtual Reality headset or helmet. VR allows us to immerse ourselves in video games as if we were one of the characters
                </p>
                <h1>Digital&Clasic ART<br><span style="font-size: 18px">by White Hog Design</span></h1>
                <p>
                    Digital painting is a relatively new but an already established art medium. It’s a medium that typically combines a computer, a graphics tablet, and software of choice. The artist uses painting and drawing techniques with the stylus that comes with the graphics tablet to create 2D paintings within a digital art software. Digital painting has evolved to not just mimic traditional art styles but fully become its own technique.
                </p>









