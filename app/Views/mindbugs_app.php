                <h2 class="text-color-dark font-weight-normal text-5 mb-2">Project <strong class="font-weight-extra-bold">Description</strong></h2>
                <p>
                    MindBugs project addresses the issues of misinformation and disinformation in an engaging and fun manner. It unites artists, journalists and programmers against misinformation to create a cool and visually appealing mobile application with Augmented Reality. It is targeted for the young generation (Gen-Z) and designed to help young people create a protective shield against the multitude of online disinformation.

                </p><p>Using machine learning we extract meaningful information from big data about fake news and then incorporated it into our mobile app. Inside the mobile app, users hunt fake news in the form of destroying mindbugs and are informed gradually about misinformation, how it works and different examples of fake news.
                </p>
                <p>From a technical perspective the project was implemented using a Unity, Vuforia and has Google Maps API Integration. </p>





