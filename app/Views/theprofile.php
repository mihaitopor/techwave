                <p class="appear-animation" data-appear-animation="fadeInUpShorter" data-appear-animation-delay="800">
                    The system presented is an artificial intelligence art installation that combines <b>technology and art</b> in order to give the viewers a meaningful experience.
                    Its performance of creating a <b>deepfake</b> of the users body in under twelve minutes along with the complex scene construction make it appealing for the public.
                    The project offers a new perspective on how to approach technology ethics on a large scale by involving people in the exploration of artificial intelligence.

                </p>
                <p class="appear-animation" data-appear-animation="fadeInUpShorter" data-appear-animation-delay="800">
                    The project has a <b>distributed architecture</b>:
                    the local component used for user interaction and the remote
                    component for heavy computation. The local component parses each frame using OpenCV library and computes the
                    image displayed on the screen.  The remote component has the objective to train as fast as possible
                    the <b>pix2pix generative adversarial neural network</b>.
                    The neural network is composed of two multilayer perceptron called the Generator and the Discriminator. The Generator
                    has to learn to compute realistic images of the body having
                    as input the skeleton.
                </p>



